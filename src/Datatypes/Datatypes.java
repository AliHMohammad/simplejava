public class Datatypes {

    public static void main(String[] args) {

        int a = 5;
        double decimal = 0.354;
        String word = new String("Hello");
        boolean bool = false;
        char bogstav = 'c';

        System.out.printf("Value of integer a is: %d", a);
        System.out.println("");
        System.out.printf("Value of double decimal is: %f", decimal);
        System.out.println("");
        System.out.printf("Value of String word is: %s", word);
        System.out.println("");
        System.out.printf("Value of boolean bool is: %b", bool);
        System.out.println("");
        System.out.printf("Value of char bogstav is: %s", bogstav);

        
    }
}